const { ensureWrite, cssVariablesReadyForObject } = require('../global.js')
const fs = require('node:fs')
const path = require('path')
const colors = require('../../src/Notations/colors/palette/palette.json')

async function getColorsPalette() {
  let colorsPalette = []
  try {
    const data = fs.readFileSync(`${path.join(__dirname, '../../src/Notations/colors/palette/palette.json')}`, 'utf8')

    const fileParsed = JSON.parse(data)
    Object.entries(fileParsed.color).forEach((entry) => {
      const [key, value] = entry;
      console.log(`${key}: ${Object.values(value)}`)
      colorsPalette.push(`"${key}": "${Object.values(value)}"`)
    })
    console.log(colorsPalette)
    return colorsPalette
  } catch (err) {
    console.error(err);
  }
}

/**
 * 
 * @param {*} content CSS variables as string
 * @returns CSS file template as string.
 */
function getStyleColorsFileTemplate(content) {
  return `/** THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY. */\n${content}`
}

/**
 *
 * @param {*} content Tokens as `key` `value` pair.
 * @returns Script file template as string.
 */
function getScriptColorsFileTemplate(content) {
  return `/** THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY. */\nexport const COLORS = {${content}}`
}

/**
 *
 * @param {*} content Tokens as `key` `value` pair.
 * @returns Common script file template as string.
 */
function getCommonScriptColorsFileTemplate(content) {
  return `/** THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY. */\nconst COLORS = {${content}}\nmodule.exports = { COLORS }`
}

async function writeColorsFile() {
  const colors = await getColorsPalette()
  try {
    await ensureWrite(`${path.join(__dirname, '../../src/Notations/colors/palette/index.scss')}`, getStyleColorsFileTemplate(colors.join('; ').replaceAll('"', '').replace(/^/,'$')).replaceAll('; ', '; $').replace(/$/,';'))
    await ensureWrite(`${path.join(__dirname, '../../src/Notations/colors/palette/index.ts')}`, getScriptColorsFileTemplate(colors.join(', ')))
    await ensureWrite(`${path.join(__dirname, '../../src/Notations/colors/palette/index.js')}`, getCommonScriptColorsFileTemplate(colors.join(', ')))
  } catch (error) {
    console.log('\x1b[31m%s\x1b[0m', 'Error writing colors file')
    console.error(error)
  }
}

async function main() {
  try {
    await writeColorsFile()
  } catch (error) {
    console.log('\x1b[31m%s\x1b[0m', 'Error generating colors file')
    console.error(error)
  }
}

void main()
